GO

CREATE TABLE USUARIOS(
	ID BIGINT IDENTITY(1,1),
	IDUsuario BIGINT NOT NULL,
	Email VARCHAR(20) NOT NULL,
	Pass VARCHAR(20) NOT NULL,
	Nombre VARCHAR(20) NOT NULL,
	Apellido VARCHAR(20) NOT NULL,
	Direccion VARCHAR(40) NULL,
